describe('most likes', () => {
  test('of empty list is null', () => {
    const result = mostLikes([])
    expect(result).toEqual(null)
  })

  test('when list has only one blog, is that author', () => {
    const result = mostLikes(listWithOneBlog)
    expect(result).toEqual({
      author: 'Edsger W. Dijkstra',
      likes: 5,
    })
  })

  test('when list has multiple blogs, is the author with most likes, author has many blogs', () => {
    const result = mostLikes(listWithMultipleBlogsMany)
    expect(result).toEqual({
      author: 'Edsger W. Dijkstra',
      likes: 17,
    })
  })

  test('when list has multiple blogs, is the author with most likes, author has one blog', () => {
    const result = mostLikes(listWithMultipleBlogsSingle)
    expect(result).toEqual({
      author: 'Michael Chan',
      likes: 20,
    })
  })

  test('when list has multiple top bloggers, gives first / any result', () => {
    const result = mostLikes(listWithMultipleTopAuths)
    const test =
      _.isEqual(result, {
        author: 'Edsger W. Dijkstra',
        likes: 17,
      }) ||
      _.isEqual(result, {
        author: 'Michael Chan',
        likes: 17,
      })
    expect(test).toBe(true)
  })
})
